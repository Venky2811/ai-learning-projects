# strings - text always in quotes
name="Venkat"
course="Gen AI"

# numbers - no quotes needed
age=21
score=95.5

# boolean - capital T and F unlike javascript's lowercase
is_learning=True

# None - same as null in javascript
result=None

# printing variables
print(name)
print(age)
print(is_learning)
print(result)

# f-string - like template literals in javascript
print(f"My name is {name} and I am {age} years old")
print(f"Am I learning AI? {is_learning}")
